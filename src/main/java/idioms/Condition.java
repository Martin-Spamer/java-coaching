
package idioms;

/**
 * Condition Class.
 */
public class Condition implements ConditionInterface<Boolean> {

	/**
	 * Instantiates a new condition.
	 */
	public Condition() {
	}

	/**
	 * Checks if is true.
	 *
	 * @return true, if is
	 * 			true
	 */
	public static boolean isTrue() {
		return true;
	}
}
