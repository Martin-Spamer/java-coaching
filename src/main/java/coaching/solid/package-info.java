/**
 * Package provides examples of the SOLID principles.
 *
 * <p>
 * The five SOLID principles of object-oriented programming.
 *
 * <p>
 * Single Responsibility principle. A class should have the responsibility for a
 * single
 * requirement, it should be possible to describe the class with a single word
 * or short phrase. It should have a single reason to change.
 *
 * <p>
 * Open Closed principle. A class should be open for extension, but closed to
 * modification.
 *
 * <p>
 * Liskov Substitution principle. A class should replaceable with instances of
 * its specialisation without altering the correctness of that program.
 *
 * <p>
 * Interface Segregation principle. A class should separate it interface from
 * its implementation. Interfaces should be fine grained to specific interfaces
 * rather than general-purpose.
 *
 * <p>
 * Dependency Inversion principle. A class should depend upon abstractions, not
 * implementations.
 */

