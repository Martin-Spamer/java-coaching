
package coaching.idioms;

/**
 * Condition Interface.
 *
 * generic type
 *
 * @param <T> the generic type
 */
public interface ConditionInterface<T> {

    /**
     * Checks if is false.
     *
     * @return true, if is
     * 			false
     */
    boolean isFalse();

    /**
     * Checks if is true.
     *
     * @return true, if is
     * 			true
     */
    boolean isTrue();

}
